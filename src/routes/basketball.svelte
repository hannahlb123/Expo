<h1 class="title">Basketball</h1>
<h2 class="subtitle">3X3 Basketball and Winter Basketball</h2>
<h2 class="subtitle" id="when">When: <br>3X3: Tuesdays from 3:45, Term 1 only <br>Winter: Fridays from 4pm, terms 2 & 3</h2>
<h2 class="subtitle" id="where">Where:<br> 3X3: Rangi Gym, <br>Winter: Various venues</h2>
<p>
  Basketball is a team sport where you have 5 players on each team. Each team
  will run down the court with a ball and try to throw the ball into the hoop
  at each end. Trials are held so that you can be sorted into teams within your year group.

  3X3 basketball is modified basketball on a half court with a max of 3 players on the court at any time. Maximum of 5 players per team. 
  
  <br><br> <b>3x3 is a SUMMER sport so sign ups are now. Winter basketball trials are also in term 1 so sign up for those now as well. </b>
</p>
<br />
<p>
  3x3 (Years 7-13): <br>
  Games every TUESDAY from 3.30pm onwards in the Rangi gym. This is a 6 week competition that starts on Tuesday 15 February & is open to Year 7-13 students of all abilities. Team lists (& individual names) sent to Mrs Brooks by Friday 4th February. Must wear your PE uniform or a basketball singlet and the cost is approx. $10 per player.  Minimum of 4-5 students per team.

  <br><br>
  Winter Basketball (YR 9-13)<br>
  All trials held in the school gym.
  Additional trials to be held Monday 29th March if needed. <br><br>

  Senior A and B Teams (Yr 11-13):  ROWERS who wish to be considered must attend the Sun 13th Feb trial. Trials - Sun 13 Feb, 6-7.30pm & Tues 29th March, 5.30-7.30pm <br><br>

  Intermediate A & B Teams (Yr 9 & 10): ROWERS who wish to be considered must attend the Mon 4th April trial. Trials - Wed 23rd Mar, 2:30-4.00pm & Mon 4th April, 3.30-5pm

  A basketball singlet is needed, you will need to purchase one at the uniform
  shop. You also need your own mouthguard. Approximately $90 for a sub + $77 for a
  singlet and transport. There are extra costs if placed into
  senior A or B team as you will go to tournament in term 3. 
</p>
