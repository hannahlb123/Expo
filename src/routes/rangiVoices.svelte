<p>
  <b>Years 7-10</b>
  rehearse together once a week to learn about choir singing through a range of
  songs. Rangi Voices gives performances at school events (e.g. Clan and Carol
  services), sings in the community - e.g with choirs from other Independent
  Schools - and takes part in the annual national Big Sing competition.
</p>
<br />
<p>
  They need to enjoy singing together and learn about what it means to be a
  positive and contributing member of a choir. It is not necessary to have
  singing lessons or necessary to be able to read music, it is an advantage
  though.
</p>
