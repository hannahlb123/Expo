<h1 class="title">Rowing</h1>
<h2 class="subtitle" id="when">
  When: October-March, trainings in the morning and after school 2-4 times per week in terms 1/4
</h2>
<h2 class="subtitle" id="where">
  Where: Trainings at Kerrs Reach, Regattas at various venues
</h2>
<p>
  You are in a team of usually 4 or 8, and you row across a stretch of water
  racing other teams to get to the finish line. There is someone
  called a cox, who sits at the front of the boat and encourages the
  rowers, this is generally someone who is small.
</p>
<br />
<p>
  Rowing is a sport which requires massive commitment, year 9s are unable to
  row in term 1 but can join in term 4. There is a monthly fee of about $750
  plus additional costs depending on where nationals take place. You will need
  the rangi rowing singlet, navy bike pants, navy socks & navy with gold stripe
  top, you will also need your own bike.
</p>
<br />

