<h2 class="subtitle" id="when">When: Meetings once a term, members are required to run chapel services according to the roster, workshops/training meetings as well.</h2>
<h2 class="subtitle" id="where">Where: Chapel</h2>
<p>
  Tech angels members primarily help run the technical side of the chapel services in collaboration with Rev Nicol, senior leaders and Dr Hastie to operate microphones, PowerPoints and videos for chapels and assemblies throughout the year. However, they will also be given training to help other students and teachers with common issues in class. There will be morning teas where food will be provided and badges will be given out once members have shown commitment.
</p>
