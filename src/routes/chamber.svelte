<h1 class="title">Chamber Music</h1>
<h2 class="subtitle" id="when">
  When: To be arranged by the individual group members
</h2>
<h2 class="subtitle" id="where">Where: Music block</h2>
<p>
  Learn about playing chamber music as a team, prepare for workshops,
  masterclasses and to enter the Chamber Music Contest. Chamber music generally
  goes until mid June, but for senior groups who make nationals, it continues
  until early August.
</p>
<br />
<p>
  You need to be able to play your instrument to a high level (Grade 5 or above)
  and a preparedness to work really hard and learn to play together with self
  discipline and direction. You also need the ability to have fun and see the
  lighter side of music making. There is a cost of entry to the chamber music
  contest. For those selected for National Finale, a cost of around $200-$300,
  depending on where the National Finale is held.
</p>
