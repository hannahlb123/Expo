<p>
  Kapa Haka is a Māori form of dance, you will learn a variety of items such as
  haka, poi, waiata, chants and action songs to enable you to perform at school
  functions, mihi whakatau etc.
</p>
<br />
<p>
  There is no cost associated with Kapa Haka, however, if you partake in events
  there may be a cost associated then. One single short poi is needed. Kapa Haka
  is for all years and you do not need to have a Māori background to join. You
  need enthusiasm to perform, learn and share kapa haka skills. There is a
  maximum of 50 people, and currently, there are 30 people in the team
</p>
