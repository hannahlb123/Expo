<h2 class="subtitle" id="when">When: Monday 5:00-7:00pm, Thursday 3.45-5.30pm</h2>
<h2 class="subtitle" id="where">Where: Music Block</h2>
<p>Enjoy singing a wide variety of music together and working hard to prepare performances of a very high standard to audiences in the Rangi, Christchurch and NZ community.</p>
<br>
<p><b>This is for years 11-13 only.</b> You need to love to sing, be willing to work hard with a fantastic group of like minded young women and to be a member of Resolutions for the entire year. An ability to read music is useful, but not essential. Singing lessons also useful, but not essential. You do not need to have been in Rangi Voices in Years 7-10 to audition, but it is a help if you have been. The cost is around $560 per year for travel to Big Sing Nationals if selected and uniform hire. There is a maximum of 30 people who can join.</p>
<br>

<h2 class="subtitle" id="signUp">You need to sign up for an audition by the 1st of January at the beginning of each year.</h2>
