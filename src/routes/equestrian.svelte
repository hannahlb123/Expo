<h2 class="subtitle" id="where">Where: Various locations</h2>
<p>
  Polo is a sport played on horse back with two teams, where you use mallets
  with long flexible handles to drive a wooden ball down a grass field between
  two goal posts. Learn to ride sessions are where you are taught how to ride a
  horse, and how to care for them. If you are an experienced rider you are also
  able to compete in events through school.
</p>
<br />
<p>
  Options include 'Learn to Ride', polo, show jumping, dressage, eventing.
  Please sign up if you are keen to be involved. Sally Goldsmith (Equestrian
  Manager) will contact you after registrations close.
  <br /><br />
  You need your own mount for events, however not required for learn to ride sessions
  or polo. The cost will depend on the level of involvment. No experience is needed,
  newcomers are welcome.
</p>
