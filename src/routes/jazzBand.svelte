<h1 class="title">Jazz Band</h1>
<h2 class="subtitle" id="when">When: Fridays 3:45-5:30pm</h2>
<h2 class="subtitle" id="where">Where: Music Block</h2>
<p>
  In Jazz Band you learn about playing jazz and improvising. Prepare for lots of
  gigs - at school, in the community - Big Band Festival, Southern Jam,
  cafes/bars/clubs and prepare for competitions.
</p>
<br />
<p>
  You must be able to read music and must be learning your instrument. An
  audition is required. Open to saxophones, trumpets and trombones. Very limited
  space for a competent pianist, competent drummer, bass player and rhythm
  guitar - there are not spaces for these players every year. The cost is around
  $500 a year to travel to Blenheim for the Southern Jam Youth Jazz Festival.
  All year groups are welcome!
</p>
