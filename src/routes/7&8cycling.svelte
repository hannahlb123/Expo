<h1 class="title">Year 7 & 8 Cycling</h1>
<h2 class="subtitle" id="when">When: Term 1 - Monday/Thursday after school <br> Term 2 - Wednesday afternoon</h2>
<h2 class="subtitle" id="where">Where: Rides leave from Rangi, terms 2 & 3 cycle in Tai Tapu</h2>
<p>Cycling is split into 3 races for the school competition- individual time trials where you race as fast as you can against the clock. Team time trials where a group of 3-4 people (max 5 for nationals) race as a team. A graded scratch where you race in a group of people about your level and it is the first person over the finish line who wins. You train as a team to learn how to do team time trials.</p>
<br>
<p><b>You will need to be confident riding to and from Tai Tapu.</b> You will need your own bike, vest and helmet. The cost is $40 per term.</p>
