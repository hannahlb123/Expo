<h1 class="title">Netball</h1>
<h2 class="subtitle" id="when">
  When: Saturdays and/or Wednesday afternoons terms 3 and 4
</h2>
<h2 class="subtitle" id="where">
  Where: Hagley park on Saturdays and various venues on Wednesdays
</h2>
<p>
  Netball is a ball sport with 7 players on each team, the object of the game is
  to throw the ball in the hoop at the end of the court. Rangi has teams
  of 10 from years 9-13, social teams are an option.
</p>
<br />
<p>
  You will need a rangi netball dress, available from the uniform shop, and navy
  shorts to wear underneath. Saturday costs between $120-$250 and Wednesdays $40 per person per term.
  Trials are held in term 1, socials teams do not need a trial. Saturday comp
  starts end of April and there will be games during the holidays. 
